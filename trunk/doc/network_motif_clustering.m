%% Network Motif Clustering Toolbox
% The Network Notif Clustering Toolbox is a toolbox for clustering
% topological network motifs in large-scale, integrated networks. The
% clustering algorithm is based on rigorous mathematical results and has
% been validated on an integrated yeast interaction network. See the
% following publication for more information:
% 
% * T. Michoel, A. Joshi, B. Nachtergaele and Y. Van de Peer,
% _Enrichment and aggregation of topological motifs are independent organizational
%   principles of integrated interaction networks_ (2011). 
%% 
% *Version 2.0* contains functions for
% 
% * <doc/html/network_motif_3_clustering_doc.html Network motif clustering> 
%
% * <doc/html/network_motif_3_significance_doc.html Calculating network motif
% aggregation and enrichment significance>
% 
% * <doc/html/random_networks_doc.html Generating random networks with fixed
% degree distribution and fixed motif count>
%%
% A complete list of functions is available from the
% <Contents.html Contents> file. Before starting, have a quick
% look at the <doc/html/network_representation_doc.html Network, tensor and motif
% representations> we use.
